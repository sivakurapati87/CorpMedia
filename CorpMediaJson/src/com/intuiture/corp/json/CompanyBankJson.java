package com.intuiture.corp.json;

public class CompanyBankJson {
	private Integer companyBankId;
	private Integer bankId;
	private String accountNumber;
	private String ifscCode;
	private String branch;
	private String bankName;
	private Integer companyId;

	public Integer getCompanyBankId() {
		return companyBankId;
	}

	public void setCompanyBankId(Integer companyBankId) {
		this.companyBankId = companyBankId;
	}

	public Integer getBankId() {
		return bankId;
	}

	public void setBankId(Integer bankId) {
		this.bankId = bankId;
	}

	public String getAccountNumber() {
		return accountNumber;
	}

	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}

	public String getIfscCode() {
		return ifscCode;
	}

	public void setIfscCode(String ifscCode) {
		this.ifscCode = ifscCode;
	}

	public String getBranch() {
		return branch;
	}

	public void setBranch(String branch) {
		this.branch = branch;
	}

	public String getBankName() {
		return bankName;
	}

	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	public Integer getCompanyId() {
		return companyId;
	}

	public void setCompanyId(Integer companyId) {
		this.companyId = companyId;
	}

}
